//Lop Cha
public class Bai1 {
	private String MaSoChuyen;
	private String HoTenTX;
	private int ChuyenXe;
	private int SoKm;
	private int DoanhThu;
	public Bai1(String maSoChuyen, String hoTenTX, int chuyenXe, int soKm, int doanhThu) {
		
		MaSoChuyen = maSoChuyen;
		HoTenTX = hoTenTX;
		ChuyenXe = chuyenXe;
		SoKm = soKm;
		DoanhThu = doanhThu;
	}
	public String getMaSoChuyen() {
		return MaSoChuyen;
	}
	public void setMaSoChuyen(String maSoChuyen) {
		MaSoChuyen = maSoChuyen;
	}
	public String getHoTenTX() {
		return HoTenTX;
	}
	public void setHoTenTX(String hoTenTX) {
		HoTenTX = hoTenTX;
	}
	public int getChuyenXe() {
		return ChuyenXe;
	}
	public void setChuyenXe(int chuyenXe) {
		ChuyenXe = chuyenXe;
	}
	public int getSoKm() {
		return SoKm;
	}
	public void setSoKm(int soKm) {
		SoKm = soKm;
	}
	public int getDoanhThu() {
		return DoanhThu;
	}
	public void setDoanhThu(int doanhThu) {
		DoanhThu = doanhThu;
	}
	
}
